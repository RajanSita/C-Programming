#include <stdio.h>

int main() {
	printf("Rajan \t BCA 1A\n");
	printf("***************\n");
    printf("Demonstrating escape sequences in C:\n");
    printf("This is a line.\nThis is a new line.\n");//New Line   
    printf("This\tis\ta\ttabbed\tline.\n");//Horizontal tab   
    printf("This is a backspace character: abc\bdef\n");//Backspace 
    printf("This is a backslash: \\\n"); //Backslash
    printf("This is a double quote: \"Hello\"\n");//Double quote
    printf("This is a single quote: \'C programming\'\n"); //Single quote
}
